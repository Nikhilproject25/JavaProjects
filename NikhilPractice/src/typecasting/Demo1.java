package typecasting;
//type casting format byte-short-int-long-float-double


//Widening Typecasting done implicitly by complier 
public class Demo1 {
	public static void main(String args[])
	{
		byte b=10;
		int i=b;
		System.out.println("i value="+i);
		
		
		float f=3.4f;
		double d=f;
		System.out.println("d value="+d);
		
	}
}
